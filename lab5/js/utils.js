function isEmailValid(email)
{
	// this regexp was found on the internet!!!
	let validEmailRegExp = 
		/^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|.(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/
	return (email.toLowerCase().match(validEmailRegExp) != null);
}